class Ticket {
    constructor(firstname, lastname, email, birthdate, accepted_agreements, from_current_user, ticketlist_index) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.email = email;
        this.birthdate = birthdate;
        this.accepted_agreements = accepted_agreements;
        this.from_current_user = from_current_user;
        this.ticketlist_index = ticketlist_index;
    }
}
